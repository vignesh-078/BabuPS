function parseDate(s) {
  if (!s || s === '*') return null;
  s = s.trim();
  const p = s.split('/');
  if (p.length !== 3) return null;
  if (p[0].length === 4) return new Date(+p[0], +p[1] - 1, +p[2]);
  return new Date(+p[2], +p[1] - 1, +p[0]);
}

async function migrate() {
  const client = new MongoClient(MONGO_URI);
  try {
    await client.connect();
    console.log('✅ Connected to MongoDB Atlas');

    const db = client.db(DB_NAME);
    const col = db.collection('loans');

    // Drop existing collection to start fresh
    const exists = await col.countDocuments();
    if (exists > 0) {
      console.log(`⚠️  Collection already has ${exists} documents. Dropping and re-inserting...`);
      await col.drop();
    }

    const docs = R.map((r) => ({
      serial:     r[0],
      name:       r[1],
      amount:     r[2] === '*' ? 0 : (parseInt(r[2]) || 0),
      dateGiven:  parseDate(r[3]),
      dateReturn: parseDate(r[4]),
      status:     r[5] || 'Active',
      item:       '',
      weight:     0,
      notes:      '',
    }));

    const result = await col.insertMany(docs);
    console.log(`✅ Successfully inserted ${result.insertedCount} loan records into '${DB_NAME}.loans'`);

    // Create indexes for performance
    await col.createIndex({ name: 1 });
    await col.createIndex({ status: 1 });
    await col.createIndex({ dateGiven: -1 });
    console.log('✅ Indexes created');

  } catch (err) {
    console.error('❌ Migration failed:', err.message);
  } finally {
    await client.close();
  }
}

